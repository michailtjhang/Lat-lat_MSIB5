<?php  
for($x = 1; $x <= 10; $x++) {
    echo '<br> bilangan 1 sampai 10 : ' . $x;
}

echo '<br> <br> ==== Bilangan 10 sampai 1 ==== ';

for ($y = 10; $y >= 1; $y--) {
    echo '<br> bilangan : ' . $y;
}

echo '<br> <br> ---- pengulangan menggunakan While ----';

$j = 1;
while ($j <= 5) {
    echo '<br> Bilangan : ' . $j;
    $j++;
}


?>